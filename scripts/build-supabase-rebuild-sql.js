"use strict";

const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const sqlDir = path.join(repoRoot, "sql");
const outputFile = path.join(sqlDir, "rebuild_supabase_full.sql");

const orderedSqlFiles = [
  "catchup_run_this.sql",
  "service_workflow_phase1.sql",
  "proposal_document_engine.sql",
  "service_recurring_plans.sql",
  "provision_failures.sql",
  "service_deposit_control.sql",
  "hydrovac_module_foundation.sql",
];

function assertReadable(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing SQL source file: ${filePath}`);
  }
}

function buildHeader() {
  return [
    "-- ProofLink full Supabase rebuild bundle",
    "-- Auto-generated by: npm run sql:build:rebuild",
    `-- Generated at (UTC): ${new Date().toISOString()}`,
    "--",
    "-- Source files are concatenated in the expected execution order.",
    "-- Do not edit this generated file directly; update the source SQL files and rebuild.",
    "",
  ].join("\n");
}

function sectionComment(fileName) {
  return [
    "",
    `-- ============================================================================`,
    `-- BEGIN ${fileName}`,
    `-- ============================================================================`,
    "",
  ].join("\n");
}

function buildBundle() {
  const chunks = [buildHeader()];
  for (const fileName of orderedSqlFiles) {
    const filePath = path.join(sqlDir, fileName);
    assertReadable(filePath);
    const sqlText = fs.readFileSync(filePath, "utf8").trimEnd();
    chunks.push(sectionComment(fileName));
    chunks.push(sqlText);
    chunks.push(`\n-- END ${fileName}\n`);
  }
  return `${chunks.join("\n")}\n`;
}

function main() {
  const bundle = buildBundle();
  fs.writeFileSync(outputFile, bundle, "utf8");
  console.log(`Wrote ${path.relative(repoRoot, outputFile)} from ${orderedSqlFiles.length} source files.`);
}

main();
